package mypack;

public class Circle {

	double r;

	public void area() {
		System.out.println("Area of the circle = " + (3.14 * r * r));
	}

}

class Square {
	double s;

	public void area() {
		System.out.println("Area of the Square = " + (s * s));
	}
}

class Rectangle {
	double l, b;

	public void area() {
		System.out.println("Area of the circle = " + (l * b));
	}
}

